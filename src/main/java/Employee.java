abstract class Employee implements Salary{
    private int id;
    private String name;
    private int age;

    public Employee(int id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "" + id + ", \"" + name + "\", " + age;
    }
}